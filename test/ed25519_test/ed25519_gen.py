import ed25519
import binascii

seed_bytes = bytes([
    0x9F, 0x3A, 0x8F, 0x3A, 0x56, 0x12, 0x7D, 0x9F, 0xB0,
    0x34, 0xA8, 0x2C, 0xFA, 0x1B, 0x17, 0xA6, 0x80, 0x99,
    0xF2, 0xD4, 0x15, 0x6A, 0x4F, 0x31, 0xC1, 0xF3, 0x2B,
    0x4F, 0xBA, 0xB3, 0xA9, 0xC4
])

private_key, public_key = ed25519.SigningKey(seed_bytes).to_seed(),ed25519.SigningKey(seed_bytes).get_verifying_key().to_bytes()

private_key_hex = binascii.hexlify(private_key).decode('utf-8')
public_key_hex = binascii.hexlify(public_key).decode('utf-8')

print("Priv ed25519:")
print(private_key_hex)
print("Pub ed25519:")
print(public_key_hex)



